package com.petsociety.models;

public class Pin {
	
	private int pinID;
	private Image image;
	private String type;
	
	
	public int getPinID() {
		return pinID;
	}
	public void setPinID(int pinID) {
		this.pinID = pinID;
	}
	public Image getImage() {
		return image;
	}
	public void setImage(Image image) {
		this.image = image;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
